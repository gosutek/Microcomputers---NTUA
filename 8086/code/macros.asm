

PRINT_CHAR MACRO CHAR
    PUSH AX
    PUSH DX
    MOV DL,CHAR
    MOV AH,2
    INT 21H
    POP DX
    POP AX
ENDM

PRINT_STR MACRO STR
    PUSH AX
    PUSH DX
    MOV DX,OFFSET STR
    MOV AH,9
    INT 21H
    POP DX
    POP AX
ENDM

